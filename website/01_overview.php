        <h1>PCGen Overview</h1>

        <p>PCGen is a Java-based RPG character generator and maintenance program (d20 Systems mainly) that works on most personal computers (Windows, Mac OS X, Linux, etc.). All datafiles are ASCII so they can be modified by users. All of this work is performed under the watchful gaze of the PCGen <a href="http://pcgen.wiki.sourceforge.net/Board%20%of%20Directors">Board Of Directors</a>.</p>

        <h2>Publishers</h2>

        <p>Are you a publisher, concerned about how PCGen may affect your product? Your concerns are understandable. Please read this <a href="https://sourceforge.net/docman/display_doc.php?docid=21131&group_id=25576" title="An open letter from Bryan McRoberts to all D20 publishers.">open letter from Bryan McRoberts</a>, the creator of PCGen. You may also be reassured to see the list of <a href="http://pcgen.sourceforge.net/autobuilds/pcgen-docs/acknowledgments/sources.html" title="Books currently available in latest upcoming version of PCGen, sorted by publisher.">other publishers</a> which have chosen to improve their game by allowing its use with PCGen.  See <a href="http://pcgen.wiki.sourceforge.net/Publishers">Publishers Wiki page</a> for more details</p>

        <h2>PCGen Features</h2>

        <ul>
            <li>Supports scores of d20 games and settings from over a dozen publishers.</li>
            <li>Highly configurable, allowing the GM to add or remove races, classes, feats, and house rules such as unusual class or race restrictions.</li>
            <li>Supports level advancement, with configurable progressions of feats, skills, and ability score bonuses.</li>
            <li>Produces numerous customized character sheets in HTML and PDF format.</li>
            <li>Produces a &quot;party&quot; sheet with information needed by the GM.</li>
        </ul>

        <h2>GMGen Plugin Features</h2>

        <ul>
            <li>Track combats, including damage, spells, and can even roll attacks. The current combat state may be saved for those game sessions which end in mid-combat.</li>
            <li>The combat tracker has a full stat block for loaded characters/enemies. If you want to see how your abilities look, you can pop up GMGen if you don't want to export a character sheet.</li>
            <li>Assign experience based on defeated combatants' CRs and the party level.</li>
            <li>Create encounters, generating the monsters on demand.</li>
            <li>Manage a campaign world through a notes system that allows word processing, as well as the ability to attach files to particular notes and the ability to launch those files/characters from the note.</li>
            <li>Generate random names for people/places/things.</li>
            <li>Calculate travel times and inn costs according to the SRD.</li>
            <li>Create 'dice bags' with any dice expression (such as 1d6-2d4+3).</li>
        </ul>

        <h2>Technical Requirements</h2>

        <p>PCGen 5.10.0+ needs to run under Java 1.5.x.  The <a href="http://java.sun.com/j2se/" title="The source for JAVA">Java runtime environment</a> is available for all common PC platforms, including Windows, Macintosh, Unix and Linux.</p>


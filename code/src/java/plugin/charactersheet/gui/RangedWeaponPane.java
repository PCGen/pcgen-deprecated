	/**
	 * Set the weapon on the PC and refresh the pane
	 * @param pc
	 * @param eq
	 */
	public void setWeapon(PlayerCharacter pc, Equipment eq)
	{
		this.pc = pc;
		this.eq = eq;
		refresh();
	}

	/**
	 * Refresh the pane
	 */
	public void refresh()
	{
		new PlayerCharacterOutput(pc);
		if (eq.isRanged())
		{
			range1.setText(WeaponToken.getRangeListToken(eq, 0, pc));
			range2.setText(WeaponToken.getRangeListToken(eq, 1, pc));
			range3.setText(WeaponToken.getRangeListToken(eq, 2, pc));
			range4.setText(WeaponToken.getRangeListToken(eq, 3, pc));
			range5.setText(WeaponToken.getRangeListToken(eq, 4, pc));

			String ammoVal = WeaponToken.getContentsToken(eq, 0);
			if (ammoVal != null && ammoVal.length() > 0)
			{
				ammo.setText(ammoVal);
				jPanelAmmo.setVisible(true);
			}
			else
			{
				ammo.setText("None");
				jPanelAmmo.setVisible(false);
			}

			range1Damage.setText(WeaponToken.getDamageToken(pc, eq, 0, 0, -1,
				false, false));
			range2Damage.setText(WeaponToken.getDamageToken(pc, eq, 1, 0, -1,
				false, false));
			range3Damage.setText(WeaponToken.getDamageToken(pc, eq, 2, 0, -1,
				false, false));
			range4Damage.setText(WeaponToken.getDamageToken(pc, eq, 3, 0, -1,
				false, false));
			range5Damage.setText(WeaponToken.getDamageToken(pc, eq, 4, 0, -1,
				false, false));

			range1ToHit.setText(WeaponToken.getTotalHitToken(pc, eq, 0, 0, -1,
				-1));
			range2ToHit.setText(WeaponToken.getTotalHitToken(pc, eq, 1, 0, -1,
				-1));
			range3ToHit.setText(WeaponToken.getTotalHitToken(pc, eq, 2, 0, -1,
				-1));
			range4ToHit.setText(WeaponToken.getTotalHitToken(pc, eq, 3, 0, -1,
				-1));
			range5ToHit.setText(WeaponToken.getTotalHitToken(pc, eq, 4, 0, -1,
				-1));

			this.setVisible(true);
		}
		else
		{
			this.setVisible(false);
		}
	}
